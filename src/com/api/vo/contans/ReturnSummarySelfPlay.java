package com.api.vo.contans;

public class ReturnSummarySelfPlay
{
  private String result;
  private String amount;
  private String win_amount;
  private String win_rate;
  
  public String getResult()
  {
    return this.result;
  }
  
  public void setResult(String result)
  {
    this.result = result;
  }
  
  public String getAmount()
  {
    return this.amount;
  }
  
  public void setAmount(String amount)
  {
    this.amount = amount;
  }
  
  public String getWin_amount()
  {
    return this.win_amount;
  }
  
  public void setWin_amount(String win_amount)
  {
    this.win_amount = win_amount;
  }
  
  public String getWin_rate()
  {
    return this.win_rate;
  }
  
  public void setWin_rate(String win_rate)
  {
    this.win_rate = win_rate;
  }
}
