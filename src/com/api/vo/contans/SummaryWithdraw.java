package com.api.vo.contans;

public class SummaryWithdraw
{
  private String result;
  private String user_sum;
  private String amount_sum;
  
  public String getResult()
  {
    return this.result;
  }
  
  public void setResult(String result)
  {
    this.result = result;
  }
  
  public String getUser_sum()
  {
    return this.user_sum;
  }
  
  public void setUser_sum(String user_sum)
  {
    this.user_sum = user_sum;
  }
  
  public String getAmount_sum()
  {
    return this.amount_sum;
  }
  
  public void setAmount_sum(String amount_sum)
  {
    this.amount_sum = amount_sum;
  }
}
